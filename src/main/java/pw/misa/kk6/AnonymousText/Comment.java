package pw.misa.kk6.AnonymousText;

/**
 *
 * @author Isabu
 */
public class Comment {
    private String name;
    private String text;

    public Comment(String name, String text) {
        this.name = name;
        this.text = text;
    }

    public String getName() {
        return name;
    }

    public String getText() {
        return text;
    }
    
}
